var teclas ={
  LEFT: 37,
  UP: 38,
  RIGHT: 39,
  DOWN: 40
};

document.addEventListener("keydown", dibujarTeclado);
function dibujarTeclado(evento){
    switch(evento.keyCode){
      case teclas.UP:
      break;
      case teclas.DOWN:
      break;
      case teclas.LEFT:
      break;
      case teclas.RIGHT:
      break;
      default:
      break;
    }


}
